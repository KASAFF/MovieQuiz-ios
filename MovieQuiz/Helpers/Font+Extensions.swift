import UIKit


extension UIFont {
static func ypMediumFont(size: CGFloat) -> UIFont {
    return UIFont(name: "YSDisplay-Medium", size: size)!
}

static func ypBoldFont(size: CGFloat) -> UIFont {
    return UIFont(name: "YSDisplay-Bold", size: size)!
}}
